	    tcp_string[tcp_string_active][tcp_string_ptr++]=DATA_Yxl>>8;
	    tcp_string[tcp_string_active][tcp_string_ptr++]=DATA_Zxl;
	    tcp_string[tcp_string_active][tcp_string_ptr++]=DATA_Zxl>>8;
	    tcp_string[tcp_string_active][tcp_string_ptr++]=DATA_Xgyro;
	    tcp_string[tcp_string_active][tcp_string_ptr++]=DATA_Xgyro>>8;
	    tcp_string[tcp_string_active][tcp_string_ptr++]=DATA_Xgyro>>16;
	    tcp_string[tcp_string_active][tcp_string_ptr++]=DATA_Xgyro>>24;
